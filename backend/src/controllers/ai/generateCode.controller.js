export const generateCode = async (req, res) => {
  try {
    const { task } = req.body;

    const title = task.title;

    res.json({
      success: true,

      files: [
        {
          filename: "routes/index.js",
          language: "javascript",
          code: `import express from "express";

const router = express.Router();

router.get("/", (req,res)=>{
    res.json({message:"${title}"});
});

export default router;`,
        },

        {
          filename: "controllers/index.js",
          language: "javascript",
          code: `export const handler=(req,res)=>{
    res.json({
        success:true
    });
};`,
        },

        {
          filename: "README.md",
          language: "markdown",
          code: `# ${title}

Starter implementation generated by DevConnect AI Architect.`,
        },
      ],
    });
  } catch (err) {
    res.status(500).json({
      success: false,
      message: err.message,
    });
  }
};